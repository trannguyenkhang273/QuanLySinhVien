    String realPass = "";
    String matKhauChinh = null;
    void setPass(JTextField txt) {
        String matK = txt.getText();
        int len = matK.length();
        matKhauChinh = matK;
        int lenR = realPass.length();
            String sao = "";
            if (len > lenR) {
                String tam = matK.substring(lenR, len);
                realPass += tam;
            } else if (len < lenR) {
                realPass = realPass.substring(0, len);
            }
            for (int i = 0; i < realPass.length(); i++) {
                sao += "*";
            }
            txt.setText(sao);
    }
   
    

