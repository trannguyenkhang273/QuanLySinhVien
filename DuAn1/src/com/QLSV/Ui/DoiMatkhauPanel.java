    public static boolean checkNullPass(JTextField txt) {
        txt.setBackground(white);
        if (txt.getText().toString().length() > 0) {
            return true;
        } else {
            txt.setBackground(pink);
            JOptionPane.showMessageDialog(txt.getRootPane(), "Không được để trống " + txt.getName());
            return false;
        }
    }

    
    public void doiMatKhauNV(){
        txtMatKhauCu.setBackground(Color.white);
        txtMatKhauMoi.setBackground(Color.white);
        txtNhapLai.setBackground(Color.white);
        String matKhau=new String(txtMatKhauCu.getText());
        String matKhauMoi=new String(txtMatKhauMoi.getText());
        String xacNhanMKM=new String(txtNhapLai.getText());
        if(matKhau.equals(Auth.USER.getMatKhau())){
            if(matKhauMoi.equals(xacNhanMKM)){
                Auth.USER.setMatKhau(matKhauMoi);
                nvdao.update(Auth.USER);
                JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công!!");
            }else{
                txtNhapLai.setBackground(Color.pink);
                JOptionPane.showMessageDialog(this, "Mật khẩu xác nhận không trùng mật khẩu");
            }
        }else{
            txtMatKhauCu.setBackground(Color.pink);
            JOptionPane.showMessageDialog(this, "Mật khẩu cũ nhập không chính xác!");
        }
    }
