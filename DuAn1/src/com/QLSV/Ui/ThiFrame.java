    void createPanel() throws IOException, MalformedURLException, LineUnavailableException, UnsupportedAudioFileException{
        int lan2=0;
        int width= panelCauHoi.getWidth();
        int height= panelCauHoi.getHeight();
        for(int i =0 ;i<soLuong;i++){
            TabButton btn =new TabButton(i,listPanel);
            CauHoiPanel j =new CauHoiPanel(listCH.get(i),dadao.selectByMa(listCH.get(i).getMaCauHoi()),i,btn);
            Color randomColor = new Color((float)Math.random(), (float)Math.random(), (float)Math.random());
            j.setBackground(randomColor);
            listPanel.add(j);
            btn.setBackground(Color.white);
            btn.setText(String.valueOf(i+1));
            if(i>25){
                btn.setBounds(lan2*50, 30,50,30);
                lan2++;
            }else{
                btn.setBounds(i*50, 0,50,30);
            }
            listButton.add(btn);
            panelCauHoi.add(j);
            panelDieuKhien.add(btn);
            panelCauHoi.repaint();
            panelDieuKhien.repaint();
            
            
            
        }
    }
