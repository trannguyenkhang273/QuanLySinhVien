    void fillThongTin(String maDe ,String maSV){
        SinhVien sv=svdao.selectByID(maSV);
        lblMaSinhVien.setText(maSV);
        lblTenSinhVien.setText(sv.getTenSinhVien());
        
        De d=ddao.selectByID(maDe);
        lblMaBaiThi.setText(maDe);
        lblSoCauHoi.setText(String.valueOf(d.getSoCauHoi()));
        lblThoiLuong.setText(String.valueOf(d.getThoigian())+" m");
        thoigian=d.getThoigian();
        soLuong=d.getSoCauHoi();
        lblMaMon.setText(d.getMaMon());
    }
