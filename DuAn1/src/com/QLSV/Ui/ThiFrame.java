    private void btnNopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNopActionPerformed
        String sqlGiua="update diem set diemgiuaki = ? where masinhvien like ? and mamon like ?";
        String sqlCuối="update diem set diemcuoiki = ? where masinhvien like ? and mamon like ?";
        int soCauDung =0;
        if(chkEnd.isSelected()){
            for (CauHoiPanel cauHoi : listPanel) {
                if(cauHoi.trueFalse()){
                    soCauDung++;
                }
            }
            MsgBox.alert(this, "Bạn đúng "+soCauDung+" / "+soLuong);  
            De d =ddao.selectByID(lblMaBaiThi.getText());
            
            if(d.getKyThi().equals("Giữa kỳ")){
                JdbcHelper.update(sqlGiua,soCauDung/soLuong*10 ,lblMaSinhVien.getText(),lblMaMon.getText());
            }else if(d.getKyThi().equals("Cuối kỳ")){
                JdbcHelper.update(sqlCuối,soCauDung/soLuong*10,lblMaSinhVien.getText(),lblMaMon.getText() );
            }
            

            
            
            dispose();// TODO add your handling code here:
        }
        
    }//GEN-LAST:event_btnNopActionPerformed

