    void setPass() {
        String matK = txtPass.getText();
        int len = matK.length();
        matKhauChinh = matK;
        int lenR = realPass.length();

        if (conMat == 0) {
            String sao = "";
            if (len > lenR) {
                String tam = matK.substring(lenR, len);
                realPass += tam;
            } else if (len < lenR) {
                realPass = realPass.substring(0, len);
            }
            for (int i = 0; i < realPass.length(); i++) {
                sao += "*";
            }
            txtPass.setText(sao);
        } else {
            realPass = txtPass.getText();
        }
    }   
