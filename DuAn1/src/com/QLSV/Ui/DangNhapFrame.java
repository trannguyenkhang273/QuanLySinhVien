    SinhVienDAO svDAO = new SinhVienDAO();
    
    void dangnhap() throws IOException {
        String user = txtUserName.getText();
        String ktuser = user.substring(0, 2);
        if (ktuser.equalsIgnoreCase("GV")) {
            GiangVien gv = gvDAO.selectByID(user);
            if (gv == null) {
                MsgBox.alert(this, "Sai tên đăng nhập!");
            } else if (!realPass.equals(gv.getMatKhau())) {
                MsgBox.alert(this, "Sai mật khẩu!");
            } else {
                Auth.USERGV = gv;
                this.dispose();
                NhapDiemFrame gdc = new NhapDiemFrame();
                gdc.setVisible(true);;

            }
        } else if (ktuser.equalsIgnoreCase("NV") || ktuser.equalsIgnoreCase("QL")) {
            NhanVien nv = nvDAO.selectByID(user);
            if (nv == null) {
                MsgBox.alert(this, "Sai tên đăng nhập!");
            } else if (!realPass.equals(nv.getMatKhau())) {
                MsgBox.alert(this, "Sai mật khẩu!");
            } else {
                Auth.USER = nv;
                this.dispose();
                GiaoDienChinh gdc = new GiaoDienChinh();
                gdc.setVisible(true); 
            }
        }
          else if(ktuser.equalsIgnoreCase("SV")){
            SinhVien sv =  svDAO.selectByID(user);
            if(sv==null){
                 MsgBox.alert(this, "Sai tên đăng nhập!");
            }else if (!realPass.equals(sv.getMatKhau())) {
                MsgBox.alert(this, "Sai mật khẩu!");
            } else {
                String[] options = new String[2];
                options[0] = new String("Xem điểm");
                options[1] = new String("Vào thi");
//                int result = JOptionPane.showOptionDialog(this,"Bạn vào xem điểm hay thi","Xác nhận", 0,JOptionPane.INFORMATION_MESSAGE,null,options,null);
                String re=MsgBox.prompt(this, "");
                  if(re.equals("Xem điểm")){ 
                      Auth.USERSV = sv;
                        this.dispose();
                        BangDiemFrame gdc = new BangDiemFrame();
                        gdc.setVisible(true);
                  }else{
                        Auth.USERSV = sv;
                        this.dispose();
                        VaoThi gdc = new VaoThi();
                        gdc.setVisible(true);
                  }
                
            }
            
        }
    }
