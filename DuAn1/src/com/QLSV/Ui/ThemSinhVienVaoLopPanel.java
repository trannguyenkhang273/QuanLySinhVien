    void loadCboNganh(){
        List<Nganh> n =ndao.selectAll();
        cboNganh.removeAllItems();
        for (Nganh nganh : n) {
            cboNganh.addItem(nganh.getMaNganh()+ " - "+nganh.getTenNganh());
        } 
    }
    void loadCboKy(){
        cboKy.removeAllItems();
        String maNganh =cboNganh.getSelectedItem().toString().substring(0,cboNganh.getSelectedItem().toString().indexOf(" "));
        List<MonHocCuaNganh> list= mhcndao.cboThem(maNganh) ;
        for (MonHocCuaNganh monHocCuaNganh : list) {
            cboKy.addItem(String.valueOf(monHocCuaNganh.getMaky()));
        }
        
    }
    void loadCboLop(){
        String maNganh =cboNganh.getSelectedItem().toString().substring(0,cboNganh.getSelectedItem().toString().indexOf(" "));
        List<Lop> mh =ldao.cboThem(maNganh,Integer.parseInt(cboKy.getSelectedItem().toString()) );
        cboMaLop.removeAllItems();
        for (Lop monHoc : mh) {
            cboMaLop.addItem(monHoc.getMaLop());
        }
    }
    void loadTblLeft(){
        String tenNganh =cboNganh.getSelectedItem().toString().substring(cboNganh.getSelectedItem().toString().lastIndexOf(" - ")+3,cboNganh.getSelectedItem().toString().length());
        DefaultTableModel model =(DefaultTableModel)tblLeft.getModel();
        model.setRowCount(0);
        List<SinhVien> list =svdao.svMoi(tenNganh);
        for (SinhVien sinhVien : list) {
            model.addRow(new Object[]{sinhVien.getMaSinhVien(),sinhVien.getTenSinhVien(),sinhVien.getCmnd()});
        }
    }
    void loadTblRight(){
        DefaultTableModel model =(DefaultTableModel)tblRight.getModel();
        model.setRowCount(0);
        List<SinhVien> list =svdao.tblRight(cboMaLop.getSelectedItem().toString());
        for (SinhVien sinhVien : list) {
            model.addRow(new Object[]{sinhVien.getMaSinhVien(),sinhVien.getTenSinhVien(),sinhVien.getCmnd()});
        }
    }
