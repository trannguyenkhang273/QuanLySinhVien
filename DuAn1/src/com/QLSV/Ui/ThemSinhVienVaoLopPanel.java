    private void cboKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboKyActionPerformed
        if(cboKy.getItemCount()==0){
            return;
        }
        cboMaLop.removeAllItems();
        clearTableLeft();clearTableRight();
        if(Integer.parseInt(cboKy.getSelectedItem().toString())==1){
            loadTblLeft();
        }
        
        loadCboLop(); 
    }//GEN-LAST:event_cboKyActionPerformed

