    private void btnNext1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNext1MouseClicked
        if(cboMaLop.getItemCount()==0){
            MsgBox.alert(this, "Chưa có lớp !");
            return;
        }
        int row[]=tblLeft.getSelectedRows();
        int soLan= row.length;
        for (int i = 0; i < soLan; i++) {
            String maSV =tblLeft.getValueAt(row[i], 0).toString();
            lsvdao.insert(new LopSinhVien( maSV, cboMaLop.getSelectedItem().toString()));
        }
        loadTblRight();
        loadTblLeft();        // TODO add your handling code here:
    }//GEN-LAST:event_btnNext1MouseClicked

    private void btnBack1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBack1MouseClicked
        int row[]=tblRight.getSelectedRows();
        int soLan= row.length;
        for (int i = 0; i < soLan; i++) {
            String maSV =tblRight.getValueAt(row[i], 0).toString();
            lsvdao.delete(cboMaLop.getSelectedItem().toString(), maSV );
        }
        loadTblLeft();
        loadTblRight();        // TODO add your handling code here:
    }//GEN-LAST:event_btnBack1MouseClicked


