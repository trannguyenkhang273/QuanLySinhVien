    private void btnNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNextMouseClicked
        if(cboLop.getItemCount()==0){
            MsgBox.alert(this, "Chưa có lớp !");
            return;
        }
        int row[]=tblLeft.getSelectedRows();
        int soLan= row.length;
        for (int i = 0; i < soLan; i++) {
            String maSV =tblLeft.getValueAt(row[i], 0).toString();
            String maMon =cboMon.getSelectedItem().toString().substring(0,cboMon.getSelectedItem().toString().indexOf(" "));
            ddao.themSV(maSV,maMon);
            lsvdao.insert(new LopSinhVien( maSV, cboLop.getSelectedItem().toString()));
        }
        loadTblRight();
        loadTblLeft();

    }//GEN-LAST:event_btnNextMouseClicked

