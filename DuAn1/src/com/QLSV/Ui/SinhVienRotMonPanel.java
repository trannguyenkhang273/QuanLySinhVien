    private void btnBackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBackMouseClicked
        
        int row[]=tblRight.getSelectedRows();
        int soLan= row.length;
        for (int i = 0; i < soLan; i++) {
            String maSV =tblRight.getValueAt(row[i], 0).toString();
            String maMon =cboMon.getSelectedItem().toString().substring(0,cboMon.getSelectedItem().toString().indexOf(" "));
            lsvdao.delete(cboLop.getSelectedItem().toString(), maSV );
            ddao.updateOutLop(maSV,maMon);
        }
        loadTblLeft();
        loadTblRight();
        
    }//GEN-LAST:event_btnBackMouseClicked
