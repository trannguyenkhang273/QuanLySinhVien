    void loadCboNganh(){
        List<Nganh> n =ndao.selectAll();
        cboNganh.removeAllItems();
        for (Nganh nganh : n) {
            cboNganh.addItem(nganh.getMaNganh()+ " - "+nganh.getTenNganh());
        } 
    }
    void loadCboKy(){
        cboKy.removeAllItems();
        String maNganh =cboNganh.getSelectedItem().toString().substring(0,cboNganh.getSelectedItem().toString().indexOf(" "));
        List<MonHocCuaNganh> list= mhcndao.cboThem(maNganh) ;
        for (MonHocCuaNganh monHocCuaNganh : list) {
            cboKy.addItem(String.valueOf(monHocCuaNganh.getMaky()));
        }
        
    }
    void loadCboMon(){
        String maNganh =cboNganh.getSelectedItem().toString().substring(0,cboNganh.getSelectedItem().toString().indexOf(" "));
        List<MonHoc> mh =mhdao.cboThem(Integer.parseInt(cboKy.getSelectedItem().toString()),maNganh );
        cboMon.removeAllItems();
        for (MonHoc monHoc : mh) {
            cboMon.addItem(monHoc.getMaMon()+" - "+monHoc.getTenMon());
        }
    }
    void loadCboLop(){
        String maNganh =cboNganh.getSelectedItem().toString().substring(0,cboNganh.getSelectedItem().toString().indexOf(" "));
        List<Lop> mh =ldao.cboThem(maNganh,Integer.parseInt(cboKy.getSelectedItem().toString()) );
        cboLop.removeAllItems();
        for (Lop monHoc : mh) {
            cboLop.addItem(monHoc.getMaLop());
        }
    }
    void loadTblRight(){
        DefaultTableModel model =(DefaultTableModel)tblRight.getModel();
        model.setRowCount(0);
        List<SinhVien> list =svdao.tblRight(cboLop.getSelectedItem().toString());
        for (SinhVien sinhVien : list) {
            model.addRow(new Object[]{sinhVien.getMaSinhVien(),sinhVien.getTenSinhVien(),sinhVien.getCmnd()});
        }
    }
    void loadTblLeft(){
        String maNganh =cboNganh.getSelectedItem().toString().substring(cboNganh.getSelectedItem().toString().lastIndexOf(" - ")+3,cboNganh.getSelectedItem().toString().length());
        String maMon =cboMon.getSelectedItem().toString().substring(0,cboMon.getSelectedItem().toString().indexOf(" "));
        DefaultTableModel model =(DefaultTableModel)tblLeft.getModel();
        model.setRowCount(0);
        List<SinhVien> list =svdao.FillTblLeft(maNganh,maMon);
        for (SinhVien sinhVien : list) {
            model.addRow(new Object[]{sinhVien.getMaSinhVien(),sinhVien.getTenSinhVien(),sinhVien.getCmnd()});
        }
    }
