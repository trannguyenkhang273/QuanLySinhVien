    void loadTableLop(){
        try {
             List<Diem> list= ddao.selectSinhVien(cboMonHoc.getSelectedItem().toString(), cboLop.getSelectedItem().toString());
            DefaultTableModel model =(DefaultTableModel)tblLop.getModel();
            model.setRowCount(0);
            for (Diem sinhVien : list) {
                if(checkTrung(sinhVien.getMaSinhVien())==0){
                        model.addRow(new Object[]{sinhVien.getMaSinhVien(),sinhVien.getTenSV(), sinhVien.getDiemGiuaKi()==-1?"":sinhVien.getDiemGiuaKi(), sinhVien.getDiemCuoiKi()==-1?"":sinhVien.getDiemCuoiKi()}); 
                }
            }
        } catch (Exception e) {
        }
    }
    int checkTrung(String masv){
        for(int j =0;j<tblDanhSachThi.getRowCount();j++){
              if(masv.equals(tblDanhSachThi.getValueAt(j, 1))){
                  return 1;
              }  
            }
        return 0;
    }
