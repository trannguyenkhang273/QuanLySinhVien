    void loabTableDe(){
        List<De> list= dedao.selectByMonHoc(cboMonHoc.getSelectedItem().toString());
        DefaultTableModel model =(DefaultTableModel)tblMonHoc.getModel();
        model.setRowCount(0);
        for (De sinhVien : list) {
            model.addRow(new Object[]{sinhVien.getMaDe(),sinhVien.getKyThi(),sinhVien.getNgayMo(),sinhVien.getNgayDong(),sinhVien.getMaGiangVien()}); 
        }
    }
