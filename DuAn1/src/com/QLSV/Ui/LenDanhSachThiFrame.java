    private void btnNext1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNext1MouseClicked
        try {
            String made = tblMonHoc.getValueAt(tblMonHoc.getSelectedRow(), 0).toString();
            String maMon =cboMonHoc.getSelectedItem().toString();     
            int rowC= tblLop.getSelectedRows().length;
            for (int i = 0; i < rowC; i++) {
                String masv=tblLop.getValueAt(tblLop.getSelectedRows()[i], 0).toString();
                dstdao.insert(new DanhSachThi(masv, made));
            }
            DefaultTableModel model =(DefaultTableModel )tblDanhSachThi.getModel();
            model.setRowCount(0);
            List<DanhSachThi> list=dstdao.selectAll();
            for (DanhSachThi danhSachThi : list) {
                model.addRow(new Object[]{danhSachThi.getMaDe(),danhSachThi.getMaSinhVien(),maMon,cboLop.getSelectedItem()});
            }
            loadTableDanhSachThi();
            loadTableLop();
        } catch (Exception e) {
            MsgBox.alert(this, "Vui lóng chọn thông tin thích hợp");
        }
        
    }//GEN-LAST:event_btnNext1MouseClicked
  void loadTableDanhSachThi() throws SQLException{
        DefaultTableModel model =(DefaultTableModel )tblDanhSachThi.getModel();
        model.setRowCount(0);
        List<DanhSachThi> list=dstdao.selectByMon(cboMonHoc.getSelectedItem().toString());
        System.out.println(list.size());
        for (DanhSachThi danhSachThi : list) {
            model.addRow(new Object[]{danhSachThi.getMaDe(),danhSachThi.getMaSinhVien()});
        }
        loadTableLop();
  }
