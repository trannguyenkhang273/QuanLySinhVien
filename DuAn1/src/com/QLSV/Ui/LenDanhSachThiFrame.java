    void fillCboMon() {
        try {
            String lop = (String) cboLop.getSelectedItem();
            Lop lophoc = ldao.selectByID(lop);
            List<LichGiangDay> lich = lgddao.selectMon(Auth.USERGV.getMaGiangVien(), lop, lophoc.getMaky(), lophoc.getMaNganh());
            DefaultComboBoxModel cboModel = (DefaultComboBoxModel) cboMonHoc.getModel();
            cboModel.removeAllElements();
            for (LichGiangDay mon : lich) {
                cboModel.addElement(mon.getMaMon());
            }
        } catch (Exception e) {
            //MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            //e.printStackTrace();
        }
    }
